package com.interest.ids.common.project.bean.analysis;



public class StationAnalysisTime {
	

	private Long startTime;
	
	private Long endTime;
	
	private Long updateTime;
	
	private Long lastCollectTime;
	
	private Long startLsFive;
	
	private Long times;
	

	public Long getStartTime() {
		return startTime;
	}

	public void setStartTime(Long startTime) {
		this.startTime = startTime;
	}

	public Long getEndTime() {
		return endTime;
	}

	public void setEndTime(Long endTime) {
		this.endTime = endTime;
	}

	public Long getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Long updateTime) {
		this.updateTime = updateTime;
	}

	public Long getLastCollectTime() {
		return lastCollectTime;
	}

	public void setLastCollectTime(Long lastCollectTime) {
		this.lastCollectTime = lastCollectTime;
	}

	public Long getStartLsFive() {
		return startLsFive;
	}

	public void setStartLsFive(Long startLsFive) {
		this.startLsFive = startLsFive;
	}

	public Long getTimes() {
		return times;
	}

	public void setTimes(Long times) {
		this.times = times;
	}
}
